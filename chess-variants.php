              Classical chess is fine and fun. However, variety might add some spice to your chess life. They are fun, they are surprisingly instructive. Here are some chess variants you might want to try
This tournament will have 3 rounds-
<br><br>
Round 1: Four Player Chess. - Four-Way chess is a fun version of standard chess. Four players play on a special board. The game improves patience – you have to wait for three others before you can make a move!
<br>
Normal chess rules apply, except:<br>
1. The game is over and the winner is the one who checkmates any of the other three kings!<br>
2. Your pawns promote on your 8th rank!<br>
3. One player moves after the next, clockwise.
<br>
Top 3 participants in each round will be promoted to the next round.
<br><br>
Round 2: Fog of War - Chess is a game where concrete calculations always are indispensable. But what happens when players must risk it all and leap into the darkness without knowing what they will meet? Enters Fog of War, one of the most exciting chess variants developed.
In this variant, players can see only the squares where their pieces can legally move. Players can't see which enemy piece captures one of their own unless at least one other piece is "looking" at the square where the capture takes place. The same is true for pawn promotions—players can only see that an opponent has been promoted if they have a piece eyeing the promotion square.
The rules of Fog of War chess are mostly the same as standard chess. The only (and crucial) difference is the absence of checks or checkmates in this variant. The game only ends when one of the kings is captured.
<br>
The player who loses will get eliminated from the tournament and rest will be promoted to the next round.
<br><br>
Round 3: Dice Chess - Dice chess can refer to a number of chess variants in which dice are used to alter gameplay; specifically that the moves available to each player are determined by rolling a pair of ordinary six-sided dice. 
The players alternate rolling the dice and, if possible, moving. On each die, the 1 each side of the dice represents pawn, rook, bishop, knight, queen and king. The player may move either of the pieces indicated on the two dice. For example, a player rolling a pawn and a rook may move either a pawn or a rook. Otherwise, standard chess rules apply, with these exceptions:
<br>
In Dice Chess the game format will be 15 mins rapid, first move can be played irrespective of the dice, the second move will be according to the dice. If the player can’t play any move on the dice then the participant will get 2 more chances to roll the dice and at the 3rd chance if the participant still can’t move a piece then he/she will be eliminated.
